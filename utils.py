import copy
import torch
from torchvision import datasets, transforms
import numpy as np

def get_dataset(args):
    """ Returns train and test datasets and a user group which is a dict where
    the keys are the user index and the values are the corresponding data for
    each of those users.
    """

    if args.dataset == 'cifar':
        data_dir = './data/cifar/'
       
        train_transform = transforms.Compose([
            transforms.RandomCrop(32, padding=4),
            transforms.RandomHorizontalFlip(),
            transforms.ToTensor(),
            transforms.Normalize((0.4914, 0.4822, 0.4465), (0.2023, 0.1994, 0.2010)),
        ])
        test_transform = transforms.Compose([
            transforms.ToTensor(),
            transforms.Normalize((0.4914, 0.4822, 0.4465), (0.2023, 0.1994, 0.2010)),
        ])
        train_dataset = datasets.CIFAR10(data_dir, train=True, download=False,
                                       transform=train_transform)

        test_dataset = datasets.CIFAR10(data_dir, train=False, download=False,
                                      transform=test_transform)
        
        # sample training data amongst users
        if args.iid:
            # Sample IID user data from Mnist
            user_groups = cifar_iid(train_dataset, args.num_users)
        else:
            # Sample Non-IID user data from Mnist
            if args.unequal:
                # Chose uneuqal splits for every user
                raise NotImplementedError()
            else:
                # Chose euqal splits for every user
                user_groups = cifar_noniid(train_dataset, args.num_users)
    elif args.dataset == 'mnist':
        data_dir = './data/mnist/'
        transform =  transforms.Compose([transforms.ToTensor(), transforms.Normalize((0.1307,), (0.3081,))])
        train_dataset = datasets.MNIST(data_dir, train=True, download=True, transform=transform)
        test_dataset = datasets.MNIST(data_dir, train=False, download=True, transform=transform)
        if args.iid:
            # Sample IID user data from Mnist
            user_groups = mnist_iid(train_dataset, args.num_users)
        else:
            # Sample Non-IID user data from Mnist
            if args.unequal:
                # Chose uneuqal splits for every user
                raise NotImplementedError()
            else:
                # Chose euqal splits for every user
                user_groups = mnist_noniid(train_dataset, args.num_users)

    return train_dataset, test_dataset, user_groups

def average_weights(w):
    """
    Returns the average of the weights.
    """
    w_avg = copy.deepcopy(w[0])
    for key in w_avg.keys():
        for i in range(1, len(w)):
            w_avg[key] += w[i][key]
        w_avg[key] = torch.div(w_avg[key], len(w))
    return w_avg

def get_weight_difference(weight1, weight2):
    difference = {}
    res = []
    for name, layer in weight1.items():
        difference[name] = layer.data - weight2[name].data
        res.append(difference[name].view(-1))

    difference_flat = torch.cat(res)

    return difference, difference_flat

def clip_grad(norm_bound, weight_difference, difference_flat):
    l2_norm = torch.norm(difference_flat.clone().detach().cuda())
    print(f'l2_norm: {l2_norm}')
    scale =  max(1.0, float(torch.abs(l2_norm / norm_bound)))
    for name in weight_difference.keys():
        if weight_difference[name].type() == 'torch.cuda.LongTensor':
            weight_difference[name] = weight_difference[name].to(torch.long) / scale
        else:
            weight_difference[name] /= scale

    return weight_difference, l2_norm


